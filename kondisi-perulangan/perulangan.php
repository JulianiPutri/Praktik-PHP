<?php

    // ===================================
    // PERULANGAN FOR
    // ===================================

    for($i=0; $i<5; $i++){
        echo "product $i";
        echo "<br>";
    }
    echo "<br>";

    // ===================================
    // PERULANGAN WHILE
    // ===================================

    $j = 0;

    while($j<5){
        echo "product $j";
        echo "<br>";

        $j++;
    }
    
    echo "<br>";
    // ===================================
    // PERULANGAN DO WHILE
    // ===================================

    $k = 0;

    do{
        echo "product $k";
        echo "<br>";

        $k++;
    }while($k<5)
?>